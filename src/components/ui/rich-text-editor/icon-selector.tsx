interface IconSelectorProps {
  isOpen: boolean;
  onSelectIcon: (icon: string) => void;
  onClose: () => void;
}

const IconButton = ({
  icon,
  onClick,
}: {
  icon: string;
  onClick: () => void;
}) => {
  return (
    <button
      onClick={onClick}
      className="text-2xl p-2 border border-gray-200 rounded-md hover:bg-gray-100 transition-colors flex items-center justify-center w-[48px] h-[48px] aspect-square"
    >
      {icon}
    </button>
  );
};

export const IconSelector: React.FC<IconSelectorProps> = ({
  isOpen,
  onSelectIcon,
  onClose,
}) => {
  const handleIconSelect = (icon: string) => {
    onSelectIcon(icon);
    onClose();
  };

  return (
    <Modal isOpen={isOpen} onClose={onClose} title="Select Icon" size="large">
      <div className="space-y-6">
        {ICON_CATEGORIES.map((category) => (
          <div key={category.name}>
            <Typography variant="h5" className="mb-2">
              {category.name}
            </Typography>
            <div className="flex flex-wrap gap-2">
              {category.icons.map((icon) => (
                <IconButton
                  key={icon}
                  icon={icon}
                  onClick={() => handleIconSelect(icon)}
                />
              ))}
            </div>
          </div>
        ))}
      </div>
    </Modal>
  );
};
